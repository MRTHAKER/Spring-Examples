package book.books;

public class Books {
	String Brand;
	String Type;
	String ForGender;
	int Price;
	int Size;
	String AvailableOn;
	
	public String getBrand() {
		return Brand;
	}
	public void setBrand(String brand) {
		Brand = brand;
	}
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}
	public String getForGender() {
		return ForGender;
	}
	public void setForGender(String forGender) {
		ForGender = forGender;
	}
	public int getPrice() {
		return Price;
	}
	public void setPrice(int price) {
		Price = price;
	}
	public int getSize() {
		return Size;
	}
	public void setSize(int size) {
		Size = size;
	}
	public String getAvailableOn() {
		return AvailableOn;
	}
	public void setAvailableOn(String availableOn) {
		AvailableOn = availableOn;
	}
}
